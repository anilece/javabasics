import java.util.Scanner;
class CheckSign{
 public static void main(String[] args){ 
   Scanner scan=new Scanner(System.in);
   int i=scan.nextInt();
   if (i>0){
      System.out.print("Positive");
   }
   else if(i==0){
      System.out.print("Zero");
   }
   else{
      System.out.print("Negative");
   }
 }
}
